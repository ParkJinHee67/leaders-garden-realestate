-- ====================================================
-- 설명: 새로운 부동산용 홈페이지 개설 시, 새 Supabase 프로젝트의
--       SQL Editor에 이 스크립트를 전체 복사하여 한 번에 실행하면
--       모든 테이블, 정책, 스토리지 버킷 및 초기 데이터를 설정할 수 있습니다.

-- ----------------------------------------------------
-- 1. 테이블 생성
-- ----------------------------------------------------

-- 중개업소 및 기본 설정 테이블
CREATE TABLE IF NOT EXISTS public.agencies (
  id uuid default gen_random_uuid() primary key,
  name text not null,
  ceo_name text not null,
  phone text,
  address text,
  email text,
  notification_phone text,
  registration_number text,
  naver_map_client_id text,
  emailjs_service_id text,
  emailjs_template_id text,
  emailjs_public_key text,
  hero_image_url text,
  ceo_image_url text,
  map_lat numeric,
  map_lng numeric,
  slogan_main text,
  slogan_sub text,
  hours_weekday text,
  hours_saturday text,
  hours_sunday text,
  hours_holiday text,
  notice_text text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 아파트/상가 등 단지 정보 테이블
CREATE TABLE IF NOT EXISTS public.complexes (
  id bigint generated by default as identity primary key,
  complex_name text not null unique,
  image_url text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 매물 종류 및 거래 그룹(공통코드용)
CREATE TABLE IF NOT EXISTS public.code_groups (
  id text primary key,
  name text not null,
  description text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 공통코드 정보 테이블
CREATE TABLE IF NOT EXISTS public.common_codes (
  id uuid default gen_random_uuid() primary key,
  group_id text references public.code_groups(id) on delete cascade,
  code_value text not null,
  code_name text not null,
  sort_order integer default 0,
  is_active boolean default true,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  UNIQUE(group_id, code_value)
);

-- 핵심 매물 정보 테이블 (V2)
CREATE TABLE IF NOT EXISTS public.properties_v2 (
  id bigint generated by default as identity primary key,
  agency_id uuid references public.agencies(id) on delete cascade,
  complex_id bigint references public.complexes(id) on delete set null,
  title text not null,
  address text not null,
  property_type_code text not null,
  transaction_type_code text not null,
  price_main bigint not null,
  price_monthly bigint default 0,
  description text not null,
  size text not null,
  rooms integer not null,
  baths integer not null,
  "isRecommended" boolean default false,
  "isUrgent" boolean default false,
  image text not null,
  asil_id text,
  naver_id text,
  status text default '광고중',
  verification_type text,
  registration_period text,
  extra_images text[] default '{}',
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 고객 상담 문의 신청 테이블
CREATE TABLE IF NOT EXISTS public.consultations (
  id bigint generated by default as identity primary key,
  name text not null,
  phone text not null,
  type text not null,
  content text not null,
  status text default '대기중' not null,
  reply text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- ----------------------------------------------------
-- 2. RLS (Row Level Security) 설정 및 퍼블릭 정책 추가
-- ----------------------------------------------------
ALTER TABLE public.agencies ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.complexes ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.code_groups ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.common_codes ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.properties_v2 ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.consultations ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow public read/write for agencies" ON public.agencies USING (true) WITH CHECK (true);
CREATE POLICY "Allow public read/write for complexes" ON public.complexes USING (true) WITH CHECK (true);
CREATE POLICY "Allow public read/write for code_groups" ON public.code_groups USING (true) WITH CHECK (true);
CREATE POLICY "Allow public read/write for common_codes" ON public.common_codes USING (true) WITH CHECK (true);
CREATE POLICY "Allow public read/write for properties_v2" ON public.properties_v2 USING (true) WITH CHECK (true);
CREATE POLICY "Allow public read/write for consultations" ON public.consultations USING (true) WITH CHECK (true);

